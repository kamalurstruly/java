import java.util.Scanner;

public class shorthand {
    public static void main(String[] args){
        Scanner Sc = new Scanner(System.in);
        int a = 7;
        int x1 = Sc.nextInt();
        a = a + x1;
        System.out.println(a);
        int x2 = Sc.nextInt();
        a = a + x2;
        System.out.println(a);
        int x3 = Sc.nextInt();
        a = a + x3;
        System.out.println(a);
        int x4 = Sc.nextInt();
        a = a + x4;
        System.out.println(a);
    }
}
